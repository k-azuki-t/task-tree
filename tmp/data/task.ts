export type chat = {
    cid: string,
    user: user,
    content: string,
    createdAt: Date,
    updatedAt: Date,
}

export type task = {
    tid: string,
    name: string,
    assignee: user,
    reviewer: user,
    priority: taskPriority,
    status: taskStatus,
    point: number,
    description: string,
    parentTask: string | null,
    tag: string[],
    createdAt: Date,
    startedAt: Date,
    finishedAt: Date,
    dueDate: Date,
    chat: chat[],
}


